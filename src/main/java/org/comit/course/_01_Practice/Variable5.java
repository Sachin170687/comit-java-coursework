package org.comit.course._01_Practice;

/*
5.	Write a Java program that declares and initializes two float variables with the 
values 5.3 and 7.5, respectively. Then, print the variables out in the console.
*/


public class Variable5 {

	public static void main(String[] args) {
		float num1=5.3f;
		float num2=7.5f;
		
		System.out.println("The first number is:" + num1);
		System.out.println("The second number is:" + num2);
		

	}

}
